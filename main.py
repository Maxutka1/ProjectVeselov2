def add(a, b):
    """
    Суммирует два числа.

    Args:
        a (int): Первое число.
        b (int): Второе число.

    Returns:
        int: Сумма двух чисел.
    """
    return a + b

def multiply(a, b):
    """
    Перемножает два числа.

    Args:
        a (int): Первое число.
        b (int): Второе число.

    Returns:
        int: Произведение двух чисел.
    """
    return a * b
